#include "libft.h"

int		ft_atoi(const char *str)
{
	register int	i;
	int				n;

	i = 0;
	n = 1;
	while ((*str >= 0x08 && *str <= 0x0d) || *str == 0x20)
		str++;
	if (*str == '-' || *str == '+')
		n = *str++ == '-' ? -1 : 1;
	while (*str && (*str >= 0x30 && *str <= 0x39))
		i = i * 10 + (*str++ - 0x30);
	return (i * n);
}
