#include "libft.h"
#define MIN(x, y) ((x) < (y) ? (x) : (y))

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t z;

	z = ft_strlen(s1) + 1;
	return (ft_memcmp(s1, s2, MIN(z, n)));
}
