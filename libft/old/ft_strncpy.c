#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char *p;

	p = dest;
	while (n && *src)
	{
		*p++ = *src++;
		--n;
	}
	while (n)
	{
		*p++ = 0x00;
		--n;
	}
	return (dest);
}
